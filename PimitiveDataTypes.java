//Write a Java program to display the default value of all primitive data types of Java.

public class PimitiveDataTypes {
    static boolean a;
    static int b;
    static char c;
    static byte d;
    static short e;
    static long f;
    static float g;
    static double h;

    public static void main(String[] args) {
        System.out.println("Default value of boolean type = " + a);
        System.out.println("Default value of int type = " + b);
        System.out.println("Default value of char type = " + c);
        System.out.println("Default value of byte type = " + d);
        System.out.println("Default value of short type = " + e);
        System.out.println("Default value of long type = " + f);
        System.out.println("Default value of float type = " + g);
        System.out.println("Default value of double type = " + h);
    }
}
